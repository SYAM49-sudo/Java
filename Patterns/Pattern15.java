public class Pattern15
    { 
public static void main(String[] args) { 
   int i,j,k=0;
    for(i=0;i<5;i++){ 
       for(j=0;j<=k;j++){
           System.out.print("1 ");
          } 
       k = k+2; 
      System.out.println(); 
    } 
  } 
} 

